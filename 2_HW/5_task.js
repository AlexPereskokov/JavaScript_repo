let firstNum = 15, secondNum = 10;

function sumNumber(x, y) {
    return x + y;
}

function subNumber(x, y) {
    return x - y;
}

function divNumber(x, y) {
    return x / y;
}

function mulNumber(x, y) {
    return x * y;
}

console.log(sumNumber(firstNum, secondNum))
console.log(subNumber(firstNum, secondNum))
console.log(divNumber(firstNum, secondNum))
console.log(mulNumber(firstNum, secondNum))
